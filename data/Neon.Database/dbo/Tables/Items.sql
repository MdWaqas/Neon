CREATE TABLE [dbo].[Items] (
    [Id]                  INT             IDENTITY (1, 1) NOT NULL,
    [ContradictionId]     INT             NULL,
    [PrecautionId]        INT             NULL,
    [DoseId]              INT             NULL,
    [PregnancyCategoryId] INT             NULL,
    [PRCOTCId]            INT             NULL,
    [ProductFormId]       INT             NULL,
    [GenericCategoryId]   INT             NULL,
    [StorageConditionId]  INT             NULL,
    [PackId]              INT             NULL,
    [InteractionId]       INT             NULL,
    [ManufacturerId]      INT             NOT NULL,
    [RackId]              INT             NULL,
    [CategoryId]          INT             NULL,
    [Name]                NVARCHAR (4000) NOT NULL,
    [Quantity]            INT             NOT NULL,
    [MinimumQuantity]     INT             NOT NULL,
    [Code]                NVARCHAR (100)  NULL,
    [PurchasePrice]       DECIMAL (18, 4) NOT NULL,
    [RetailPrice]         DECIMAL (18, 4) NOT NULL,
    [CreatedBy]           NVARCHAR (320)  NULL,
    [CreatedDate]         DATETIME2 (4)   CONSTRAINT [DF_Items_CreatedDate] DEFAULT (getutcdate()) NOT NULL,
    [ModifiedBy]          NVARCHAR (320)  NULL,
    [ModifiedDate]        DATETIME2 (4)   CONSTRAINT [DF_Items_ModifiedDate] DEFAULT (getutcdate()) NOT NULL,
    [Status]              INT             NOT NULL,
    [RowVersion]          ROWVERSION      NOT NULL,
    [Ingredint]           NVARCHAR (4000) NULL,
    [Instruction]         NVARCHAR (4000) NULL,
    [Indications]         NVARCHAR (4000) NULL,
    [SideEffects]         NVARCHAR (4000) NULL,
    CONSTRAINT [PK_dbo.Items] PRIMARY KEY CLUSTERED ([Id] ASC),
    CONSTRAINT [FK_dbo.Items_dbo.Categories_CategoryId] FOREIGN KEY ([CategoryId]) REFERENCES [dbo].[Categories] ([Id]) ON DELETE CASCADE,
    CONSTRAINT [FK_dbo.Items_dbo.Contradictions_ContradictionId] FOREIGN KEY ([ContradictionId]) REFERENCES [dbo].[Contradictions] ([Id]) ON DELETE CASCADE,
    CONSTRAINT [FK_dbo.Items_dbo.Doses_DoseId] FOREIGN KEY ([DoseId]) REFERENCES [dbo].[Doses] ([Id]) ON DELETE CASCADE,
    CONSTRAINT [FK_dbo.Items_dbo.GenericCategories_GenericCategoryId] FOREIGN KEY ([GenericCategoryId]) REFERENCES [dbo].[GenericCategories] ([Id]) ON DELETE CASCADE,
    CONSTRAINT [FK_dbo.Items_dbo.Interactions_InteractionId] FOREIGN KEY ([InteractionId]) REFERENCES [dbo].[Interactions] ([Id]),
    CONSTRAINT [FK_dbo.Items_dbo.Manufacturers_ManufacturerId] FOREIGN KEY ([ManufacturerId]) REFERENCES [dbo].[Manufacturers] ([Id]) ON DELETE CASCADE,
    CONSTRAINT [FK_dbo.Items_dbo.Packs_PackId] FOREIGN KEY ([PackId]) REFERENCES [dbo].[Packs] ([Id]) ON DELETE CASCADE,
    CONSTRAINT [FK_dbo.Items_dbo.PRCOTCs_PRCOTCId] FOREIGN KEY ([PRCOTCId]) REFERENCES [dbo].[PRCOTCs] ([Id]) ON DELETE CASCADE,
    CONSTRAINT [FK_dbo.Items_dbo.Precautions_PrecautionId] FOREIGN KEY ([PrecautionId]) REFERENCES [dbo].[Precautions] ([Id]) ON DELETE CASCADE,
    CONSTRAINT [FK_dbo.Items_dbo.PregnancyCategories_PregnancyCategoryId] FOREIGN KEY ([PregnancyCategoryId]) REFERENCES [dbo].[PregnancyCategories] ([Id]) ON DELETE CASCADE,
    CONSTRAINT [FK_dbo.Items_dbo.ProductForms_ProductFormId] FOREIGN KEY ([ProductFormId]) REFERENCES [dbo].[ProductForms] ([Id]) ON DELETE CASCADE,
    CONSTRAINT [FK_dbo.Items_dbo.Racks_RackId] FOREIGN KEY ([RackId]) REFERENCES [dbo].[Racks] ([Id]) ON DELETE CASCADE,
    CONSTRAINT [FK_dbo.Items_dbo.StorageConditions_StorageConditionId] FOREIGN KEY ([StorageConditionId]) REFERENCES [dbo].[StorageConditions] ([Id]) ON DELETE CASCADE
);



